class CBSTemplatePublic
{
	/**************************************************************************/

	function output($file,$data=array(),$echo=true)
	{
		$this->data=$data;
		ob_start();
 		include(PLUGIN_CBS_TEMPLATE_PATH.'public/'.$file.'.php');
		$value=ob_get_clean();
		
		if($echo) echo $value;
		else return($value);
	}
	
	/**************************************************************************/
}

