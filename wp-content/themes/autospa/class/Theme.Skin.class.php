class Autospa_ThemeSkin
{
	/**************************************************************************/
	
	function __construct()
	{
		$this->skin=array
		(
			'general'															=>	array
			(
				'landing_url'													=>	'http://quanticalabs.com/wp_themes4/'
			),
			'skin'																=>	array
			(
				1																=>	array
				(			
					'name'														=>	__('Default','autospa')
				)
			)
		);
	}
	
	/**************************************************************************/
	
	function getSkinDictionary()
	{
		return($this->skin);
	}

	/**************************************************************************/
	
	function getSkin()
	{
		return(get_option(AUTOSPA_THEME_SKIN_OPTION_NAME,1));
	}
	
	/**************************************************************************/
}

